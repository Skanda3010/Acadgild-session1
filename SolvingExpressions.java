
public class SolvingExpressions {

	public static void main(String[] args) {
		int x=5,y=10;
		System.out.println("Value of expression  x + y * 2 is " + ( x + y * 2));
		System.out.println("Value of expression  x - y + 2 is " + (  x - y + 2));
		System.out.println("Value of expression  (x+y) * 2 is " + ( (x+y) * 2));
		System.out.println("Value of expression  y % x is " + ( y % x));

	}

}
